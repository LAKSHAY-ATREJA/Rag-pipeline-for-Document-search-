"""
demo.py — offline demonstration of the RAG pipeline.

This script does not require a running Streamlit server. It:
  1. Creates a small in-memory document corpus (no PDF file needed).
  2. Splits the corpus into chunks.
  3. Embeds the chunks with the local HuggingFace model.
  4. Builds a FAISS vector index.
  5. Runs several natural-language queries against the index.
  6. (Optional) calls the Groq LLM to generate answers when
     GROQ_API_KEY is set in the environment.

Run:
    python3 demo.py

Environment variables (optional):
    GROQ_API_KEY   — if set, answers will be generated by Groq Llama3.
                     If not set, the script prints retrieved chunks only.
"""

